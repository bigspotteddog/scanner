package com.neuvector.model;

public class BenchItem{
    String test_number;
    String catalog;
    String type;
    String profile;
    boolean scored;
    String description;
    String remediation;
    String[] tags;
    String level;
    String evidence;
    String location;
    String[] message;
    String group;

    
    /** 
     * @return String
     */
    public String getLevel() {
        return level;
    }
    
    /** 
     * @param level String
     */
    public void setLevel(String level) {
        this.level = level;
    }
    
    /** 
     * @return String
     */
    public String getEvidence() {
        return evidence;
    }
    
    /** 
     * @param evidence String
     */
    public void setEvidence(String evidence) {
        this.evidence = evidence;
    }
    
    /** 
     * @return String
     */
    public String getLocation() {
        return location;
    }
    
    /** 
     * @param location String
     */
    public void setLocation(String location) {
        this.location = location;
    }
    
    /** 
     * @return String[]
     */
    public String[] getMessage() {
        return message;
    }
    
    /** 
     * @param message String[]
     */
    public void setMessage(String[] message) {
        this.message = message;
    }
    
    /** 
     * @return String
     */
    public String getGroup() {
        return group;
    }
    
    /** 
     * @param group String
     */
    public void setGroup(String group) {
        this.group = group;
    }
    
    /** 
     * @return String
     */
    public String getTest_number() {
        return test_number;
    }
    
    /** 
     * @param test_number String
     */
    public void setTest_number(String test_number) {
        this.test_number = test_number;
    }
    
    /** 
     * @return String
     */
    public String getCatalog() {
        return catalog;
    }
    
    /** 
     * @param catalog String
     */
    public void setCatalog(String catalog) {
        this.catalog = catalog;
    }
    
    /** 
     * @return String
     */
    public String getType() {
        return type;
    }
    
    /** 
     * @param type String
     */
    public void setType(String type) {
        this.type = type;
    }
    
    /** 
     * @return String
     */
    public String getProfile() {
        return profile;
    }
    
    /** 
     * @param profile String
     */
    public void setProfile(String profile) {
        this.profile = profile;
    }
    
    /** 
     * @return boolean
     */
    public boolean isScored() {
        return scored;
    }
    
    /** 
     * @param scored boolean
     */
    public void setScored(boolean scored) {
        this.scored = scored;
    }
    
    /** 
     * @return String
     */
    public String getDescription() {
        return description;
    }
    
    /** 
     * @param description String
     */
    public void setDescription(String description) {
        this.description = description;
    }
    
    /** 
     * @return String
     */
    public String getRemediation() {
        return remediation;
    }
    
    /** 
     * @param remediation String
     */
    public void setRemediation(String remediation) {
        this.remediation = remediation;
    }
    
    /** 
     * @return String[]
     */
    public String[] getTags() {
        return tags;
    }
    
    /** 
     * @param tags String[]
     */
    public void setTags(String[] tags) {
        this.tags = tags;
    }

    
}
